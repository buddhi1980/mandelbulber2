#ifndef MANDELBULBER2_SRC_PROGRESS_TEXT_HPP_
#define MANDELBULBER2_SRC_PROGRESS_TEXT_HPP_

#include <QProgressBar>
#include <QStatusBar>
#include <QtCore>

class cProgressText
{
public:
	enum enumProgressType
	{
		progress_IMAGE,
		progress_ANIMATION,
		progress_QUEUE
	};
	cProgressText();
	void ResetTimer();
	QString getText(double progress);
	double getTime() const { return timer.elapsed() / 1000.0; }

private:
	QElapsedTimer timer;
	QString TimeString(qint64 time) const;
	qint64 lastTimeForETA;
	double lastProgressForETA;
	double renderingSpeed;

public:
	static void ProgressStatusText(const QString &text, const QString &progressText, double progress,
		enumProgressType progressType = progress_IMAGE);
};

#endif /* MANDELBULBER2_SRC_PROGRESS_TEXT_HPP_ */
